#include "../headers/МатематическийКласс.h"

float МатематическийКласс::Вычисление_корня(float номер)
{
    long и;
    float а2, б;
    const float три_с_половиной = 1.5f;

    а2 = номер * 0.5F;
    б = номер;
    и = *(long *) &б;
    и = 0x5F375A86 - (и >> 1);
    б = *(float *) &и;
    б = б * (три_с_половиной - (а2 * б * б));
    б = б * (три_с_половиной - (а2 * б * б));
    return б;
}
